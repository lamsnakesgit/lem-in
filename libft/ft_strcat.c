#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	size_t i;
	size_t g;

	i = 0;
	g = 0;
	while (s1[i])
		i++;
	while (s2[g])
	{
		s1[i] = s2[g];
		i++;
		g++;
	}
	s1[i] = '\0';
	return (s1);
}
